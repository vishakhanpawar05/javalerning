package Array;

public class SingalArray {

	public static void main (String[] args) {
		//singal dimension array
		
		int a[] = {11,22,33,44,55};
		int ar[] = new int[5];
		
		ar[0]=11;
		ar[1]=22;
		ar[2]=33;
		ar[3]=44;
		ar[4]=55;
		
		for(int i=0; i<a.length;i++)
			System.out.println(ar[i]);
		}
}
